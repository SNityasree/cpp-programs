#include<iostream>
#include<math.h>
using namespace std;

int main()
{
    int a;
    int b;
    cin>>a;
    cin>>b;
    cout<<a<<" + "<<b<<" = "<<(a+b)<<"\n";
    cout<<a<<" - "<<b<<" = "<<(a-b)<<"\n";
    cout<<a<<" * "<<b<<" = "<<(a*b)<<"\n"<<a<<" / "<<b<<" = "<<(a/b)<<"\n"<<a<<" % "<<b<<" = "<<(a%b);
    
    return 0;
}
